-- 创建表结构
CREATE TABLE products (
    product_id SERIAL PRIMARY KEY,
    sku VARCHAR(20) UNIQUE NOT NULL,
    name VARCHAR(100) NOT NULL,
    category VARCHAR(20) NOT NULL,
    version VARCHAR(10) NOT NULL,
    net_weight INT NOT NULL,
    shelf_life SMALLINT NOT NULL,
    production_cost NUMERIC(8,2) NOT NULL,
    retail_price NUMERIC(8,2) NOT NULL,
    is_active BOOLEAN DEFAULT TRUE,
    launch_date DATE NOT NULL,
    tech_patent VARCHAR(20)
);

CREATE TABLE inventory (
    batch_id VARCHAR(15) PRIMARY KEY,
    product_id INT REFERENCES products(product_id),
    production_date DATE NOT NULL,
    expiry_date DATE NOT NULL,
    warehouse VARCHAR(10) NOT NULL,
    stock_quantity INT NOT NULL,
    quality_report_no VARCHAR(20)
);

CREATE TABLE ingredients (
    ingredient_id SERIAL PRIMARY KEY,
    product_id INT REFERENCES products(product_id),
    name VARCHAR(50) NOT NULL,
    concentration VARCHAR(20) NOT NULL,
    supplier VARCHAR(50) NOT NULL,
    function TEXT
);

